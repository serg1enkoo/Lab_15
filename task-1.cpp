#include <iostream>  // Підключення бібліотеки для вводу/виводу, яка забезпечує доступ до std::cout.
using namespace std; // Дозволяє використовувати елементи простору імен std без префікса "std::".

// Функція isLeap перевіряє, чи є переданий рік високосним.
// Аргумент:
//    int year – рік, який потрібно перевірити.
// Повертає:
//    bool – true, якщо рік високосний, або false, якщо рік не високосний.
bool isLeap(int year) {
    // Правила високосного року:
    // 1. Рік високосний, якщо він кратний 4, але не кратний 100.
    // 2. Рік високосний, якщо він кратний 400.
    //
    // Операція % (залишок від ділення) використовується для визначення кратності.
    // Наприклад, якщо (year % 4) дорівнює 0, то рік ділиться на 4 без залишку.
    //
    // Логічне вираження нижче перевіряє:
    // - (year % 4 == 0 && year % 100 != 0):
    //      Рік ділиться на 4, але не ділиться на 100 (умова для більшості високосних років).
    // - (year % 400 == 0):
    //      Рік ділиться на 400 (ця умова покриває випадки, коли рік ділиться на 100, але все одно високосний).
    //
    // Якщо хоча б одна з умов виконується, функція повертає true.
    return ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0));
}

// Функція main – точка входу в програму.
int main(void) {
    // Цикл for, який проходить по роках від 1995 до 2016 включно.
    // Для кожного року викликається функція isLeap, а її результат виводиться на екран.
    for (int yr = 1995; yr < 2017; yr++) {
        // Виводимо рік та результат роботи функції isLeap.
        // Якщо функція повертає true, на екрані з’явиться "1", якщо false – "0".
        cout << yr << " -> " << isLeap(yr) << endl;
    }
    // Повертаємо 0, що означає успішне завершення програми.
    return 0;
}
